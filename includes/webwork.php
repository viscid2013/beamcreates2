		<p>Suggest a Purchase was a system built to manage suggestions from patrons as to books and other library materials for purchase.</p>
		<p>The front end includes an entry form for the suggestions that uses the Google Books API to pull author and title information using the ISBN as well as an ODBC connection to the Intergrated Library System database to check whether or not the item is already owned by the lirbary system.</p>
